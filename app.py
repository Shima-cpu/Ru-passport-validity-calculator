# ===== –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏ =====
def safe_add_years(d: date, years: int) -> date:
    """–î–æ–±–∞–≤–∏—Ç—å –≥–æ–¥—ã –∫ –¥–∞—Ç–µ —Ä–æ–∂–¥–µ–Ω–∏—è, –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—è 29 —Ñ–µ–≤—Ä–∞–ª—è."""
    try:
        return d.replace(year=d.year + years)
    except ValueError:
        # –ï—Å–ª–∏ 29 —Ñ–µ–≤—Ä–∞–ª—è -> –±–µ—Ä—ë–º 28 —Ñ–µ–≤—Ä–∞–ª—è —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–≥–æ –≥–æ–¥–∞
        return d.replace(month=2, day=28, year=d.year + years)


def classify_passport_stage(birth: date, issue: date) -> str:
    """–û–ø—Ä–µ–¥–µ–ª–∏—Ç—å, –Ω–∞ –∫–∞–∫–æ–º –≤–æ–∑—Ä–∞—Å—Ç–Ω–æ–º —ç—Ç–∞–ø–µ –±—ã–ª –≤—ã–¥–∞–Ω –¢–ï–ö–£–©–ò–ô –ø–∞—Å–ø–æ—Ä—Ç."""
    d14 = safe_add_years(birth, 14)
    d20 = safe_add_years(birth, 20)
    d45 = safe_add_years(birth, 45)

    # –î–æ–ø—É—Å–∫–∞–µ–º 90-–¥–Ω–µ–≤–Ω—ã–π –∫–æ—Ä–∏–¥–æ—Ä –ø–æ—Å–ª–µ –ø–æ—Ä–æ–≥–æ–≤–æ–π –¥–∞—Ç—ã —Ä–æ–∂–¥–µ–Ω–∏—è
    if d14 <= issue <= d20 + timedelta(days=90):
        return "–ü–µ—Ä–≤–∏—á–Ω–æ–µ –ø–æ–ª—É—á–µ–Ω–∏–µ –≤ 14 –ª–µ—Ç"
    if d20 <= issue <= d45 + timedelta(days=90):
        return "–û–±–º–µ–Ω –≤ 20 –ª–µ—Ç"
    if issue >= d45:
        return "–û–±–º–µ–Ω –≤ 45 –ª–µ—Ç"
    return "–ù–µ–æ–±—ã—á–Ω–∞—è –¥–∞—Ç–∞ –≤—ã–¥–∞—á–∏ (–ø—Ä–æ–≤–µ—Ä—å—Ç–µ –≤–≤–æ–¥)"


def compute_next_change(birth: date, today: date) -> dict:
    """–†–∞—Å—Å—á–∏—Ç–∞—Ç—å —Å–ª–µ–¥—É—é—â—É—é –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—É—é –∑–∞–º–µ–Ω—É –∏ –∫—Ä–∞–π–Ω–∏–π —Å—Ä–æ–∫ (90 –¥–Ω–µ–π)."""
    d20 = safe_add_years(birth, 20)
    d45 = safe_add_years(birth, 45)

    if today < d20:
        stage = "–ü–µ—Ä–≤–∞—è –∑–∞–º–µ–Ω–∞ –≤ 20 –ª–µ—Ç"
        next_change = d20
    elif today < d45:
        stage = "–í—Ç–æ—Ä–∞—è –∑–∞–º–µ–Ω–∞ –≤ 45 –ª–µ—Ç"
        next_change = d45
    else:
        return {
            "stage": "–ü–æ—Å–ª–µ 45 –ª–µ—Ç: –≤–æ–∑—Ä–∞—Å—Ç–Ω—ã—Ö –∑–∞–º–µ–Ω –±–æ–ª—å—à–µ –Ω–µ—Ç",
            "next_change": None,
            "deadline": None,
            "status": "–ü–æ—Å–ª–µ 45 –ª–µ—Ç –ø–∞—Å–ø–æ—Ä—Ç —Å—á–∏—Ç–∞–µ—Ç—Å—è –±–µ—Å—Å—Ä–æ—á–Ω—ã–º (–±–µ–∑ –≤–æ–∑—Ä–∞—Å—Ç–Ω—ã—Ö –∑–∞–º–µ–Ω).",
        }

    deadline = next_change + timedelta(days=90)
    if today > deadline:
        status = "‚ùå –°—Ä–æ–∫ –∑–∞–º–µ–Ω—ã –ø—Ä–æ—Å—Ä–æ—á–µ–Ω (–ø—Ä–æ—à–ª–æ –±–æ–ª–µ–µ 90 –¥–Ω–µ–π –ø–æ—Å–ª–µ –¥–Ω—è —Ä–æ–∂–¥–µ–Ω–∏—è)."
    elif today >= next_change:
        days = (deadline - today).days
        status = f"‚ö†Ô∏è –¢—Ä–µ–±—É–µ—Ç—Å—è –∑–∞–º–µ–Ω–∞. –î–æ –∫—Ä–∞–π–Ω–µ–≥–æ —Å—Ä–æ–∫–∞ –æ—Å—Ç–∞–ª–æ—Å—å {days} –¥–Ω."
    else:
        days = (next_change - today).days
        status = f"‚úÖ –ü–∞—Å–ø–æ—Ä—Ç –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª–µ–Ω. –î–æ –¥–∞—Ç—ã –∑–∞–º–µ–Ω—ã –æ—Å—Ç–∞–ª–æ—Å—å {days} –¥–Ω."

    return {
        "stage": stage,
        "next_change": next_change,
        "deadline": deadline,
        "status": status,
    }


def validate_inputs(birth: date, issue: date, today: date) -> list[str]:
    """–ë–∞–∑–æ–≤—ã–µ –ø—Ä–æ–≤–µ—Ä–∫–∏ –≤–∞–ª–∏–¥–Ω–æ—Å—Ç–∏ –≤–≤–æ–¥–∞."""
    errs = []

    # –û–±—â–∏–µ –ø—Ä–æ–≤–µ—Ä–∫–∏
    if birth > today:
        errs.append("–î–∞—Ç–∞ —Ä–æ–∂–¥–µ–Ω–∏—è –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –≤ –±—É–¥—É—â–µ–º.")
    if issue > today:
        errs.append("–î–∞—Ç–∞ –≤—ã–¥–∞—á–∏ –ø–∞—Å–ø–æ—Ä—Ç–∞ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –≤ –±—É–¥—É—â–µ–º.")
    if issue < birth:
        errs.append("–î–∞—Ç–∞ –≤—ã–¥–∞—á–∏ –ø–∞—Å–ø–æ—Ä—Ç–∞ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å —Ä–∞–Ω—å—à–µ –¥–∞—Ç—ã —Ä–æ–∂–¥–µ–Ω–∏—è.")

    # –í–æ–∑—Ä–∞—Å—Ç < 14 –ª–µ—Ç ‚Äî –ø–∞—Å–ø–æ—Ä—Ç –µ—â—ë –Ω–µ –≤—ã–¥–∞–µ—Ç—Å—è
    if relativedelta(today, birth).years < 14:
        errs.append("–õ–∏—Ü—É –º–ª–∞–¥—à–µ 14 –ª–µ—Ç –ø–∞—Å–ø–æ—Ä—Ç –µ—â—ë –Ω–µ –≤—ã–¥–∞–µ—Ç—Å—è.")

    # –í—ã–¥–∞—á–∞ —Ä–∞–Ω—å—à–µ 14-–ª–µ—Ç–∏—è ‚Äî –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ
    d14 = safe_add_years(birth, 14)
    if issue < d14:
        errs.append("–ü–∞—Å–ø–æ—Ä—Ç –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –≤—ã–¥–∞–Ω —Ä–∞–Ω–µ–µ –¥–æ—Å—Ç–∏–∂–µ–Ω–∏—è 14 –ª–µ—Ç. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –¥–∞—Ç—É –≤—ã–¥–∞—á–∏.")

    # –ì—Ä–∞–Ω–∏—Ü—ã –≤–∏–¥–∂–µ—Ç–∞ (–¥–æ–ø.–ø—Ä–æ–≤–µ—Ä–∫–∞ ‚Äî –Ω–∞ —Å–ª—É—á–∞–π —Ä—É—á–Ω–æ–π –ø—Ä–∞–≤–∫–∏ —á–µ—Ä–µ–∑ query params)
    if not (MIN_BIRTH <= birth <= MAX_BIRTH):
        errs.append(f"–î–∞—Ç–∞ —Ä–æ–∂–¥–µ–Ω–∏—è –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ {MIN_BIRTH.strftime('%d.%m.%Y')}‚Äì{MAX_BIRTH.strftime('%d.%m.%Y')}.")
    if not (MIN_ISSUE <= issue <= MAX_ISSUE):
        errs.append(f"–î–∞—Ç–∞ –≤—ã–¥–∞—á–∏ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ {MIN_ISSUE.strftime('%d.%m.%Y')}‚Äì{MAX_ISSUE.strftime('%d.%m.%Y')}.")

    return errs


